//! Phase 10: HTML reporter.
//!
//! Renders a `ScanReport` to a self-contained HTML document — inline CSS,
//! no external CDNs, no JavaScript. The output is the canonical artifact
//! for both the `--output html` mode and the PDF reporter (which lifts
//! data out of the same `ScanReport` directly).

use std::fmt::Write;

use crate::models::finding::{CertFinding, HttpFinding, Recommendation, TlsFinding, Vulnerability};
use crate::models::report::ScanReport;
use crate::models::risk::RiskLevel;

pub struct HtmlReporter;

impl HtmlReporter {
    pub fn render(report: &ScanReport) -> String {
        let mut out = String::with_capacity(8 * 1024);
        write_doc(&mut out, report);
        out
    }
}

fn write_doc(buf: &mut String, report: &ScanReport) {
    let color = report.risk_score.level.color_code();
    let level = report.risk_score.level.label();
    // An unreachable scan has no meaningful numeric score — show a dash so it
    // can't be misread as a "0 = perfect" result.
    let score = if report.risk_score.level == RiskLevel::Unknown {
        "—".to_string()
    } else {
        report.risk_score.value.to_string()
    };

    let _ = write!(
        buf,
        "<!DOCTYPE html>\n<html lang=\"en\">\n<head>\n\
         <meta charset=\"UTF-8\">\n\
         <title>RonwayScanner — {domain}</title>\n\
         <style>{css}</style>\n\
         </head>\n<body>\n",
        domain = esc(&report.target.domain),
        css = embedded_css(),
    );

    // Header / risk badge
    let _ = write!(
        buf,
        "<header class=\"banner\">\n\
           <h1>RonwayScanner</h1>\n\
           <p class=\"tagline\">Know your quantum risk before it knows you.</p>\n\
         </header>\n\
         <section class=\"summary\">\n\
           <div class=\"target\">\n\
             <h2>{domain}<span class=\"port\">:{port}</span></h2>\n\
             <p class=\"meta\">Scanned at {scanned_at} &middot; {duration} ms</p>\n\
           </div>\n\
           <div class=\"score\" style=\"background:{color}\">\n\
             <div class=\"score-value\">{score}</div>\n\
             <div class=\"score-level\">{level}</div>\n\
           </div>\n\
         </section>\n",
        domain = esc(&report.target.domain),
        port = report.target.port,
        scanned_at = esc(&report.target.scanned_at),
        duration = report.target.scan_duration_ms,
        color = color,
        score = score,
        level = level,
    );

    let _ = write!(
        buf,
        "<section class=\"executive\">\n<p>{summary}</p>\n",
        summary = esc(&report.summary)
    );
    if report.risk_score.harvest_risk {
        buf.push_str(
            "<p class=\"harvest\"><strong>Harvest-now-decrypt-later risk:</strong> \
             quantum-vulnerable key exchange is in use, so today's encrypted traffic \
             may be decrypted by a future quantum adversary.</p>\n",
        );
    }
    buf.push_str("</section>\n");

    write_tls_section(buf, &report.tls);
    write_cert_section(buf, &report.certificate);
    write_http_section(buf, &report.http);
    write_vulns_section(buf, &report.vulnerabilities);
    write_recommendations_section(buf, &report.recommendations);

    let _ = write!(
        buf,
        "<footer class=\"foot\">Generated by RonwayScanner v{ver} &middot; BPxAI &middot; \
         All Rights Reserved</footer>\n</body>\n</html>\n",
        ver = env!("CARGO_PKG_VERSION"),
    );
}

fn write_tls_section(buf: &mut String, tls: &Option<TlsFinding>) {
    buf.push_str("<section><h3>TLS</h3>");
    match tls {
        Some(t) => {
            let _ = write!(
                buf,
                "<table class=\"kv\">\
                 <tr><td>Protocol</td><td>{}{}</td></tr>\
                 <tr><td>Cipher suite</td><td>{}{}</td></tr>\
                 <tr><td>Key exchange</td><td>{}{}</td></tr>\
                 <tr><td>Compression</td><td>{}</td></tr>\
                 </table>",
                esc(&t.protocol_version),
                badge(t.protocol_vulnerable),
                esc(&t.cipher_suite),
                badge(t.cipher_vulnerable),
                esc(&t.key_exchange),
                badge(t.key_exchange_vulnerable),
                esc(&t.compression),
            );
        }
        None => buf.push_str("<p class=\"warn\">TLS scan failed.</p>"),
    }
    buf.push_str("</section>");
}

fn write_cert_section(buf: &mut String, cert: &Option<CertFinding>) {
    buf.push_str("<section><h3>Certificate</h3>");
    match cert {
        Some(c) => {
            let _ = write!(
                buf,
                "<table class=\"kv\">\
                 <tr><td>Subject</td><td>{}</td></tr>\
                 <tr><td>Issuer</td><td>{}</td></tr>\
                 <tr><td>Key algorithm</td><td>{}{}</td></tr>\
                 <tr><td>Signature algorithm</td><td>{}{}</td></tr>\
                 <tr><td>Valid from</td><td>{}</td></tr>\
                 <tr><td>Valid until</td><td>{}</td></tr>\
                 <tr><td>Days remaining</td><td>{}{}</td></tr>\
                 <tr><td>Self-signed</td><td>{}</td></tr>\
                 <tr><td>CT logged</td><td>{}</td></tr>\
                 </table>",
                esc(&c.subject),
                esc(&c.issuer),
                esc(&c.key_algorithm),
                badge(c.key_algorithm_vulnerable),
                esc(&c.signature_algorithm),
                badge(c.signature_algorithm_vulnerable),
                esc(&c.valid_from),
                esc(&c.valid_until),
                c.days_remaining,
                if c.is_expired {
                    " <span class=\"b-fail\">expired</span>"
                } else {
                    ""
                },
                if c.is_self_signed { "yes" } else { "no" },
                if c.ct_logged { "yes" } else { "no" },
            );
        }
        None => buf.push_str("<p class=\"warn\">Certificate scan failed.</p>"),
    }
    buf.push_str("</section>");
}

fn write_http_section(buf: &mut String, http: &Option<HttpFinding>) {
    buf.push_str("<section><h3>HTTP headers</h3>");
    match http {
        Some(h) => {
            let hsts = if h.hsts_enabled {
                match h.hsts_max_age {
                    Some(n) => format!("enabled (max-age = {} s)", n),
                    None => "enabled (no max-age)".to_string(),
                }
            } else {
                "missing".to_string()
            };
            let _ = write!(
                buf,
                "<table class=\"kv\">\
                 <tr><td>Strict-Transport-Security</td><td>{}{}</td></tr>\
                 <tr><td>Content-Security-Policy</td><td>{}</td></tr>\
                 <tr><td>X-Frame-Options</td><td>{}</td></tr>\
                 <tr><td>Server header</td><td>{}</td></tr>\
                 </table>",
                esc(&hsts),
                if !h.hsts_enabled {
                    " <span class=\"b-fail\">no HSTS</span>"
                } else {
                    ""
                },
                if h.csp_present { "present" } else { "missing" },
                opt_or_dash(&h.x_frame_options),
                opt_or_dash(&h.server_header),
            );
        }
        None => buf.push_str("<p class=\"warn\">HTTP scan failed.</p>"),
    }
    buf.push_str("</section>");
}

fn write_vulns_section(buf: &mut String, vulns: &[Vulnerability]) {
    let _ = write!(
        buf,
        "<section><h3>Vulnerabilities <span class=\"count\">({})</span></h3>",
        vulns.len()
    );
    if vulns.is_empty() {
        buf.push_str("<p>No vulnerabilities detected.</p></section>");
        return;
    }
    buf.push_str(
        "<table class=\"vulns\">\
         <thead><tr><th>Severity</th><th>ID</th><th>Title</th><th>NIST</th><th>CVSS</th></tr></thead>\
         <tbody>",
    );
    for v in vulns {
        let _ = write!(
            buf,
            "<tr>\
             <td><span class=\"sev sev-{cls}\">{level}</span></td>\
             <td><code>{id}</code></td>\
             <td>{title}<br><small>{desc}</small></td>\
             <td>{nist}</td>\
             <td>{cvss:.1}</td>\
             </tr>",
            cls = sev_class(&v.severity),
            level = v.severity.label(),
            id = esc(&v.id),
            title = esc(&v.title),
            desc = esc(&v.description),
            nist = esc(&v.nist_reference),
            cvss = v.cvss_equivalent,
        );
    }
    buf.push_str("</tbody></table></section>");
}

fn write_recommendations_section(buf: &mut String, recs: &[Recommendation]) {
    let _ = write!(
        buf,
        "<section><h3>Recommendations <span class=\"count\">({})</span></h3>",
        recs.len()
    );
    if recs.is_empty() {
        buf.push_str("<p>No remediation actions required.</p></section>");
        return;
    }
    buf.push_str(
        "<table class=\"recs\">\
         <thead><tr><th>P</th><th>Action</th><th>Current</th><th>Replace with</th>\
         <th>Effort</th><th>NIST</th></tr></thead><tbody>",
    );
    for r in recs {
        let _ = write!(
            buf,
            "<tr><td>{}</td><td>{}</td><td>{}</td><td>{}</td><td>{} wk</td><td>{}</td></tr>",
            r.priority,
            esc(&r.action),
            esc(&r.current),
            esc(&r.replace_with),
            r.effort_weeks,
            esc(&r.nist_algorithm),
        );
    }
    buf.push_str("</tbody></table></section>");
}

fn sev_class(level: &RiskLevel) -> &'static str {
    match level {
        RiskLevel::Critical => "critical",
        RiskLevel::High => "high",
        RiskLevel::Medium => "medium",
        RiskLevel::Low => "low",
        RiskLevel::Pass => "pass",
        RiskLevel::Unknown => "unknown",
    }
}

fn badge(vulnerable: bool) -> &'static str {
    if vulnerable {
        " <span class=\"b-fail\">quantum-vulnerable</span>"
    } else {
        " <span class=\"b-ok\">ok</span>"
    }
}

fn opt_or_dash(s: &Option<String>) -> String {
    match s {
        Some(v) if !v.is_empty() => esc(v),
        _ => "—".to_string(),
    }
}

/// Tiny HTML-escape: enough for text in attribute values and element bodies.
fn esc(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for c in s.chars() {
        match c {
            '&' => out.push_str("&amp;"),
            '<' => out.push_str("&lt;"),
            '>' => out.push_str("&gt;"),
            '"' => out.push_str("&quot;"),
            '\'' => out.push_str("&#39;"),
            _ => out.push(c),
        }
    }
    out
}

