#[cfg(test)]
mod tests {
    use super::*;
    use crate::models::finding::Vulnerability;
    use crate::models::report::{ScanReport, ScanTarget};
    use crate::models::risk::{RiskLevel, RiskScore};

    fn minimal_report() -> ScanReport {
        ScanReport {
            target: ScanTarget {
                domain: "<script>alert(1)</script>".into(),
                ip_address: None,
                port: 443,
                scanned_at: "2026-05-28T00:00:00+00:00".into(),
                scan_duration_ms: 100,
            },
            risk_score: RiskScore {
                value: 35,
                level: RiskLevel::Low,
                summary: "Low risk".into(),
                harvest_risk: false,
            },
            tls: None,
            certificate: None,
            http: None,
            vulnerabilities: vec![Vulnerability {
                id: "TEST_VULN".into(),
                title: "Title & <stuff>".into(),
                description: "desc".into(),
                severity: RiskLevel::High,
                nist_reference: "FIPS 203".into(),
                cvss_equivalent: 7.4,
            }],
            recommendations: vec![],
            summary: "summary".into(),
            quantum_ready: false,
        }
    }

    #[test]
    fn html_starts_with_doctype() {
        let html = HtmlReporter::render(&minimal_report());
        assert!(html.starts_with("<!DOCTYPE html>"));
    }

    #[test]
    fn html_escapes_user_strings() {
        let html = HtmlReporter::render(&minimal_report());
        assert!(html.contains("&lt;script&gt;"));
        assert!(!html.contains("<script>alert(1)</script>"));
        assert!(html.contains("Title &amp; &lt;stuff&gt;"));
    }

    #[test]
    fn html_contains_no_external_urls() {
        let html = HtmlReporter::render(&minimal_report());
        // Self-contained requirement per CLAUDE.md.
        assert!(!html.contains("http://"));
        assert!(!html.contains("https://"));
        assert!(!html.contains("//cdn"));
    }

    #[test]
    fn html_shows_failure_messages_when_scanners_missing() {
        let html = HtmlReporter::render(&minimal_report());
        assert!(html.contains("TLS scan failed"));
        assert!(html.contains("Certificate scan failed"));
        assert!(html.contains("HTTP scan failed"));
    }
}
